#Data type 

x = 5
print(type(x))

#String type
x = "Hello World"
print(x)
print(type(x))
x = str('Hello World')
print(x)

#Integer type
x=20
print(x)
print(type(x))
x = int(20)
print(x)

#float type
a = 20.5
print(a)
print(type(a))
x = float(20)
print(x)

#complex type
x = 1j
print(x)
print(type(x))
a = complex(1)
print(a)

#list type
x = ['apple','banana','cherry']
print(x)
print(type(x))
a = list(('apple','banana','cherry'))
print(a)

#tuple type
x = ('apple','banana','cherry')
print(x)
print(type(x))
a = tuple(('apple','banana','cherry'))
print(a)

#Range 
x=range(6)
print(x)
print(type(x))


#dict type
x = {'name':'Rakib','age':22}
print(x)
print(type(x))
a = dict(name='Sakib',age=13)
print(a)

#Set type
a = set(('apple','banana','cherry'))
print(a)
print(type(a))

#Boolean type
x = True
print(x)
print(type(x))
a = bool(5)
print(a)

# Bytes type

# Python Numbers 
# There are three numeric types in python :
''' 
*int 
 *float
 *complex '''

x = 1 #int
y = 2.8 #float
z = 1j #complex

#integer number

a = 1
b = 324432543
c = -232

print(a,b,c)
print(type(a))
print(type(b))
print(type(c))

#float number

a = 1.12
b = 1.0
c = -35.59
d = 23e4
e = 12E4
print(type(a))
print(type(b))
print(type(c))
print(d)
print(e)

#Complex number

a = 3 + 5j
b = 5j
c = -4j
print(a)
print(b)
print(c)

#Convert from one type to another:

x = 1
